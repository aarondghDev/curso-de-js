// If else + else if

let nota = 5;

if (nota === 5) {
    console.log("Enhorabuena, has obtenido un sobresaliente");
} else if (nota === 4) {
    console.log("Buen trabajo pero podrias haberlo hecho mejor");
} else if (nota === 3) {
    console.log("Haz obtenido un suficiente");
} else if (nota === 2) {
    console.log("No has aprobado por poco");
} else if (nota === 1) {
    console.log("Desaprobado");
} else {
    console.log("Error, intoduce una nota entre 1 y 5");
}